#ifndef INCLUDED_NORMALBLOCK
#define INCLUDED_NORMALBLOCK
#include "Block.h"
#include <SFML\System\Vector2.hpp>
///////////////////////////////////////////////////////
// /A normal block is a stationary block.
// /Represents walls, floors, ceilings and such
///////////////////////////////////////////////////////
class NormalBlock : public Block {
public:
	///////////////////////////////////////////////////////
	// /Assign the blocks position.
	///////////////////////////////////////////////////////
	NormalBlock(sf::Vector2f position);
	///////////////////////////////////////////////////////
	// /No dynamic members
	///////////////////////////////////////////////////////
	~NormalBlock();
	///////////////////////////////////////////////////////
	// /Does nothing
	///////////////////////////////////////////////////////
	void update();
	///////////////////////////////////////////////////////
	// /Draws the block
	///////////////////////////////////////////////////////
	void render();
	///////////////////////////////////////////////////////
	// /Colliding with a normal block simply pushes the 
	// /entity colliding with the normal block out of it's
	// /hit box.
	///////////////////////////////////////////////////////
	void onCollision(Entity* e);
private:

};

#endif
