#ifndef INCLUDED_NORMALBLOCK
#define INCLUDED_NORMALBLOCK
#include "Block.h"
#include <SFML\System\Vector2.hpp>
///////////////////////////////////////////////////////
// /A normal block is a stationary block.
// /Represents walls, floors, ceilings and such
///////////////////////////////////////////////////////
class NormalBlock : public Block {
public:
	enum BlockType{
		GRAS, DIRT
	};
	///////////////////////////////////////////////////////
	// /Assign the blocks position.
	///////////////////////////////////////////////////////
	NormalBlock(sf::Vector2f position, BlockType type = GRAS);
	///////////////////////////////////////////////////////
	// /No dynamic members
	///////////////////////////////////////////////////////
	~NormalBlock();
	///////////////////////////////////////////////////////
	// /Does nothing
	///////////////////////////////////////////////////////
	void update();
	///////////////////////////////////////////////////////
	// /Draws the block
	///////////////////////////////////////////////////////
	void render(sf::RenderWindow &window);
private:
	BlockType mBlockType;
	sf::Texture mTexture;
	sf::Sprite mSprite;
};

#endif
